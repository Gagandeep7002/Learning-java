class dataTypes {
    public static void main(String[] args) {
        //integer values 
        byte a=50;//smaller range
        short b=1400;//larger range than byte
        int c=16126762;//larger range than short
        long d=612766736667198924L;//largest range

        //decimal values
        float e=3.1415F;//smaller range
        double f=1.2335672723;//larger range

        //characters
        char g='a';

        System.out.println("Values of a,b,c,d,e,f,g respectively "+a+" "+b+" "+c+" "+d+" "+e+" "+f+" "+g+" ");
        }
}
